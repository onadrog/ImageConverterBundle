<?php

namespace Onadrog\ImageConverterBundle\Mock\Entity\Entity;

use Doctrine\ORM\Mapping as ORM;
use Onadrog\ImageConverterBundle\Mock\Entity\Repository\DummyWithoutAttributeRepository;

/**
 * @ORM\Entity(repositoryClass=DummyWithoutAttributeRepository::class)
 */
class DummyWithoutAttribute
{
    /**
     * @ORM\Id
     * @ORM\GeneratedValue
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $name;

    /**
     * @ORM\ManyToOne(targetEntity=Product::class, inversedBy="dummy")
     */
    private $product;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(string $name): self
    {
        $this->name = $name;

        return $this;
    }

    public function getProduct(): ?Product
    {
        return $this->product;
    }

    public function setProduct(?Product $product): self
    {
        $this->product = $product;

        return $this;
    }
}
